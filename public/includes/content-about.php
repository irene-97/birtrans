    <p align="justify">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Грузы, как и девушки, бывают
        разные. Хорошо, когда все упаковано, завернуто, связано узелочком. Еще лучше, когда груз стандартный, имеет
        одинаковые размеры, вес, форму, легко укладывается в ящики, контейнеры и сам грузится в любой из
        существующих видов транспорта: железнодорожный, автомобильный, водный, воздушный. И вообще шикарно, когда
        груз сам мгновенно доставляется туда куда это нам необходимо и мы за это ничего не платим.
    </p>
    <p align="justify">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Так какой же вид транспорта
        дешевле?
    </p>
    <p align="justify">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;&nbsp;&nbsp;<strong>Железнодорожный
            транспорт</strong> обеспечивает возможность сравнительно быстрой доставки груза на большие расстояния, а
        также если необходимо перевезти объемный, негабаритный или тяжеловесный груз. Здесь можно эффективно
        организовывать выполнение погрузочно-разгрузочных работ. Железнодорожный транспорт, отличается сравнительно
        <em>невысокой стоимостью перевозки, наличием скидок</em>. К недостаткам железнодорожного транспорта следует
        отнести неразвитость путей, что заставляет железнодорожный транспорт дополнять автомобильным.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;&nbsp;&nbsp;<strong>Автотранспорт</strong>
        используется для перевозок на сравнительно короткие расстояния. С помощью автомобилей груз может
        доставляться &quot;от дверей до дверей&quot; с необходимой степенью срочности. На стоимость автоперевозок
        влияют многие факторы: <em>используемый транспорт, расстояние, вес или объем груза</em>. Также, стоимость
        может меняться из-за <em>сезонности, рентабельности, вида перевозки и характера груза</em>. Кроме этого,
        увеличивать стоимость могут оказываемые погрузочные или экспедиторские работы, оформление сопроводительной
        документации и др. Недостатками перевозок автотранспортом являются:<em> дороговизна, скорость разгрузки,
            возможность хищения и угона, относительно небольшая грузоподъемность</em>.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;&nbsp;&nbsp;<strong>Морской
            транспорт</strong> характеризуется <em>низкими грузовыми тарифами и высокой провозной способностью</em>.
        К недостаткам морского транспорта можно отнести низкую скорость, жесткие требования к упаковке и креплению
        грузов, малую частоту отправок. А также зависимость этого вида транспорта от погодных и навигационных
        условий и сложной портовой инфраструктуры.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;&nbsp;&nbsp;<strong>Речной и внутренний
            морской транспорт</strong> предлагает <em>низкие грузовые тарифы</em>. При перевозках грузов весом более
        100 т. на расстояние более 250 км этот вид транспорта - самый дешевый. К недостаткам внутреннего водного
        транспорта, кроме малой скорости доставки, относят также низкую доступность в географическом плане.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•&nbsp;&nbsp;&nbsp;<strong>Авиаперевозки</strong>
        характеризуются высокой скоростью, распространённостью, высокой сохранностью грузов, когда наиболее важным
        параметром является время. К недостаткам относят
        <em>высокие грузовые тарифы и зависимость от метеоусловий</em>, которая снижает надежность соблюдения
        графика поставки.
    </p>
