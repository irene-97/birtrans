        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>И сколько же это
                стоит?&nbsp;</u></strong>&nbsp;&nbsp;&nbsp;&nbsp;<em>(К вопросу о стоимости грузоперевозок)</em>
