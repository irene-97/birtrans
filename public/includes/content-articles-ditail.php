    <h3>Проблемы грузоперевозок в России</h3>
    Наша страна за последние два десятка лет стала развиваться стремительными темпами. В течении этого времени также
    стремительно начало развиваться производство товаров. Вместе с этим выросла и потребность в их транспортировке.
    <br>
    На данный момент автомобильные грузоперевозки являются одним из самых эффективных способов доставить товар из пункта
    " А " в пункт " Б ". Но существует ряд проблем, которые пагубно влияют на данную сферу. В этой статье мы попытаемся
    раскрыть эти проблемы.
    <br>

    <br>
    Начать следует с того, что автомобильные грузоперевозки никогда не были сильной стороной хозяйственной деятельности
    России. Первой, и самой главной проблемой данной отросли, являются дороги. Качество автомагистралей в нашей стране
    оставляет желать лучшего. Большинство автодорог сильно изношены и требуют капитального ремонта, а кое-где их
    отсутствие является огромной проблемой. Качество услуг автомобильных грузоперевозок из-за фактора национальных дорог
    резко падает.
    <br>

    <br>
    Во-первых, падает скорость, так как на некоторых участках дорог просто невозможно ехать со скоростью более сорока
    километров в час, чтобы не повредить ни транспортное средство ни товар в нем.
    <br>

    <br>
    Во-вторых, увеличиваются цены на саму транспортировку, так как автомобили требуют дорогого технического обслуживания
    и ремонта, передвигаясь по, практически непригодным для езды, дорогам.
    <br>

    <br>
    Другой проблемой автомобильных грузоперевозок в нашей стране является устаревшие автотранспортные средства. Многие
    из компаний предоставляющие услуги автомобильных грузоперевозок осуществляют их на старых советских автомашинах.
    Из-за этого скорость доставки грузов заметно падает. Компании, осуществляющие свою деятельность с середины 90-х
    годов на таких автомобилях. Каждый год списывают около 8 - 10 % своих транспортных средств ежегодно, поднимая цены
    на тарифы для обновления своих автопарков.
    <br>

    <br>
    Следующей немаловажной проблемой отросли являться законодательство. Наше общество стремительно развивается, и
    развиваются вместе с этим и общественные отношения. Наше законодательство в сфере автомобильных грузоперевозок было
    разработано еще в Советском Союзе в середине семидесятых годов, и применяется по сегодня. Следует учесть, что оно
    было принято во времена социализма, когда и речи не могло идти о свободных рыночных отношениях. Налоговая система
    является другой проблемой автомобильных грузоперевозок. Лишь за последние сем лет она менялась четыре раза.
    <br>

    <br>
    Сегодня водители получают заработок в зависимости от километража. Таким образом, они работают на износ, тем самым
    пренебрегая правилам дорожного движения и безопасности.
    <br>

    <br>
    Устранение данных проблем благоприятно скажется на уровне жизни граждан нашей страны. Разрешение этих вопросов дало
    бы сильнейший толчок всей экономике в целом.
