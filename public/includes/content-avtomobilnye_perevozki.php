    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>Как это
                было&nbsp;</u></strong>&nbsp;&nbsp;&nbsp;&nbsp;<em>(Исторический экскурс грузоперевозок)</em></p>

    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Когда именно в мире появились
        грузоперевозки, сказать достаточно трудно. Но история началась с незапамятных времен.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Предметы, вещи, материалы, объекты самые древние
        люди перетаскивали руками, эксплуатируя только свою физическую силу, поэтому правильней было бы назвать это -
        "переноской груза".
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;По мере совершенствования интеллектуальных
        способностей, у человека разумного появилось желание переложить свою ношу на другого. С приручением диких
        животных, самые сильные из них, начали использоваться, как с целью перевозки (вьючные), так и тягловые - для
        перемещения груза.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Революция в усовершенствовании перевозки груза
        произошла лишь после изобретения колеса. Повозка с лошадьми значительно помогла увеличить массу и габариты
        перевозимого груза.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Со 2-го века до н.э. самым крупным направлением
        грузоперевозок стал Шелковый путь. По нему нескончаемым потоком шли китайские караваны в Европу и на Восток,
        перевозя уже тонны товаров.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В 1834 году было закончено строительство первого
        шоссе в России - из Петербурга в Москву. Николай I уделял особое внимание состоянию Российских дорог. При нем
        был принят закон о дорожном строительстве, по которому все дороги в государстве были поделены на пять классов.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В настоящее время наиболее оптимальным способом
        транспортировки груза на малые расстояния, а также в труднодоступные регионы являются автомобильные
        грузоперевозки, так как их главное преимущество - мобильность. </p>

