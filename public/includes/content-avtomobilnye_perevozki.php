    <h2>Перевозка негабаритных грузов</h2>

    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Одно из ключевых направлений <strong>Транспортно-логистической
            компании Биртранс</strong> в организации и осуществлении грузоперевозок является <strong>перевозка
            негабаритного, длинномерного, тяжеловесного груза автомобильным транспортом</strong>, представляющая из себя
        сложное, серьезное и ответственное дело.</p>

    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>Негабаритный груз</strong> -
        это такой объект, технические характеристики которого (габариты, масса, многомерность), не соответствуют
        стандартам транспортировки, предусмотренными правилами безопасности на дорогах. В связи с этим перевозки
        негабаритных грузов обычными способами просто невозможны. При перевозке подобного вида грузов возникает
        необходимость в применении особых методов транспортировки: </p>

