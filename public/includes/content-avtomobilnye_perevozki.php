    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;а также требуется применение <strong>специализированного
            автотранспорта</strong>. Это так называемый <strong>низкорамник</strong>, или <strong>автомобиль с
            низкорамной платформой</strong>, способный осуществить перевозку негабарита, безопасную как для самого
        автомобиля и других участников дорожного движения, так и для самого негабаритного груза.</p>

    <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>Низкорамный трал</strong> -
        это автомобильный прицеп с различным уровнем грузоподъемности, в виде открытой грузовой платформы, с высотой
        площадки от 0,4 м. до 0,9 м. Такая конструкция платформы при перевозке негабаритных грузов позволяет не
        ограничиваться размерами кузова автомобиля и высотой самого груза. Так же бывают телескопические платформы,
        платформы с возможностью фронтальной и боковой загрузки, платформы с аппарелями. </p>

